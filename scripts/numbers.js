//* Show Numbers in Js

//? type 1 => decimal
let n1 = 20;
console.log(n1);

//? type 2 => binary
let n2 = 0b10100;
console.log(n2);

//? type 3 => Octal
let n3 = 0o24;
console.log(n3);

//? type 4 => hexadecimal
let n4 = 0xffffff;
console.log(n4);

//? type 5 => power of 10
let n5 = 2e7; // 2 * 10 ^ 7
console.log(n5);
let n6 = 2.4444e5;
console.log(n6); //2.4444 * 10 ^ 5
let n7 = 4.55555e-3; // 4.55555 * 10 ^ -3